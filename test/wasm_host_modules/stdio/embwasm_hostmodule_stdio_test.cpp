class WasmHostModuleStdioTest : public ::testing::Test {
protected:
    void SetUp() override {
        pool_.Init(g_wasm_pool_buf, sizeof(g_wasm_pool_buf));
        engine_.Init(pool_);
        instance_id_ = engine_.LoadModule(kWasmStdioTestBinary, sizeof(kWasmStdioTestBinary));
        ASSERT_GE(instance_id_, 0);
        ASSERT_EQ(engine_.InstantiateModules(), embwasm::WasmResult::kOk);
        mem_ = engine_.GetLinearMemory(instance_id_);
        mem_size_ = engine_.GetModuleInstanceById(instance_id_)->GetLinearMemorySize();
        ASSERT_NE(mem_, nullptr);
    }

    void TearDown() override {
        engine_.Deinit();
        pool_.Deinit();
    }

    embwasm::WasmMemoryPool pool_;
    embwasm::WasmEngine engine_;
    int32_t instance_id_;
    uint8_t* mem_;
    size_t mem_size_;
};

TEST_F(WasmHostModuleStdioTest, ResolveStaticFunctions) {
    embwasm::HostFunctionId func_printf = embwasm::LookupStaticHostFunctionId("sys:rt/stdio", 12, "printf", 6);
    EXPECT_EQ(func_printf, embwasm::kWasmHostFuncIdStdioPrintf);

    embwasm::HostFunctionId func_puts = embwasm::LookupStaticHostFunctionId("sys:rt/stdio", 12, "puts", 4);
    EXPECT_EQ(func_puts, embwasm::kWasmHostFuncIdStdioPuts);
}

TEST_F(WasmHostModuleStdioTest, PutsExecution) {
    const char* msg = "Hello from Stdio Test!";
    uint32_t len = static_cast<uint32_t>(std::strlen(msg));
    uint32_t addr = 100;
    std::memcpy(mem_ + addr, msg, len);

    embwasm::WasmValue puts_args[] = {
        embwasm::WasmValue::FromI32(static_cast<int32_t>(addr)),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(len))
    };
    embwasm::WasmValue puts_result[1];
    embwasm::WasmResult res = engine_.Execute(
        nullptr, 0, "test_puts", 9, puts_args, 2, puts_result, 1);
    EXPECT_EQ(res, embwasm::WasmResult::kOk);
    EXPECT_EQ(puts_result[0].value.i32, static_cast<int32_t>(len + 1));

    // 異常系: 線形メモリ範囲外のポインタ指定
    embwasm::WasmValue oob_args[] = {
        embwasm::WasmValue::FromI32(static_cast<int32_t>(mem_size_ - 5)),
        embwasm::WasmValue::FromI32(10)
    };
    embwasm::WasmValue oob_result[1];
    embwasm::WasmResult res_oob = engine_.Execute(
        nullptr, 0, "test_puts", 9, oob_args, 2, oob_result, 1);
    EXPECT_EQ(res_oob, embwasm::WasmResult::kErrorExecuteRuntimeError);
}

TEST_F(WasmHostModuleStdioTest, PrintfExecution) {
    const char* fmt = "Int: %04d, Hex: 0x%02x, Char: %c, String: %s, Percent: %%";
    uint32_t fmt_len = static_cast<uint32_t>(std::strlen(fmt));
    uint32_t fmt_addr = 200;
    std::memcpy(mem_ + fmt_addr, fmt, fmt_len);

    const char* val_str = "Antigravity";
    uint32_t val_str_addr = 300;
    std::memcpy(mem_ + val_str_addr, val_str, std::strlen(val_str) + 1);

    int32_t list_data[4] = { 99, 15, 'A', static_cast<int32_t>(val_str_addr) };
    uint32_t list_addr = 400;
    std::memcpy(mem_ + list_addr, list_data, sizeof(list_data));

    embwasm::WasmValue printf_args[] = {
        embwasm::WasmValue::FromI32(static_cast<int32_t>(fmt_addr)),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(fmt_len)),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(list_addr)),
        embwasm::WasmValue::FromI32(4)
    };
    embwasm::WasmResult res = engine_.Execute(
        nullptr, 0, "test_printf", 11, printf_args, 4, nullptr, 0);
    EXPECT_EQ(res, embwasm::WasmResult::kOk);
}

TEST_F(WasmHostModuleStdioTest, PrintfOmissionAndOOB) {
    const char* fmt = "Float: %f, Int: %d";
    uint32_t fmt_len = static_cast<uint32_t>(std::strlen(fmt));
    uint32_t fmt_addr = 500;
    std::memcpy(mem_ + fmt_addr, fmt, fmt_len);

    int32_t list_data[1] = { 123 };
    uint32_t list_addr = 600;
    std::memcpy(mem_ + list_addr, list_data, sizeof(list_data));

    embwasm::WasmValue printf_args[] = {
        embwasm::WasmValue::FromI32(static_cast<int32_t>(fmt_addr)),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(fmt_len)),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(list_addr)),
        embwasm::WasmValue::FromI32(1)
    };
    embwasm::WasmResult res = engine_.Execute(
        nullptr, 0, "test_printf", 11, printf_args, 4, nullptr, 0);
    EXPECT_EQ(res, embwasm::WasmResult::kOk);

    // 異常系: 線形メモリ範囲外のフォーマット文字列
    embwasm::WasmValue oob_args[] = {
        embwasm::WasmValue::FromI32(static_cast<int32_t>(mem_size_ - 2)),
        embwasm::WasmValue::FromI32(5),
        embwasm::WasmValue::FromI32(static_cast<int32_t>(list_addr)),
        embwasm::WasmValue::FromI32(1)
    };
    embwasm::WasmResult res_oob = engine_.Execute(
        nullptr, 0, "test_printf", 11, oob_args, 4, nullptr, 0);
    EXPECT_EQ(res_oob, embwasm::WasmResult::kErrorExecuteRuntimeError);
}
